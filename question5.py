PI = 3.14#the value of phi
#input is taken from key board so input function is used
r = float(input('Enter the radius of a circle:'))#as the value is displayed in decimals we use float

area = PI * (r * r)#area of circle formula

circumference = 2 * PI * r#circumference of circle formula

print("AREA of circle is ",area)
print("Circumference of circle is: ",circumference)
